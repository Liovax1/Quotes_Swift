/*:
 ## Exercise - For-In Loops
 [En savoir plus sur les Boucles](https://www.pixel-swift.com/project/boucle-for-in/ "Les Boucles")


 Créez une boucle for-in qui parcourt les valeurs de 1 à 100 et affiche chacune de ces valeurs.
 */
for i in 1...100
{
    print(i)
}
/*:
 Créez un tableau actors contenant vos acteurs préférés. Utilisez une boucle for-in pour imprimer chaque acteur.
 */
var actors:[String] = ["Paul Adelstain", "Jeremy Kent Jackson"]
for i in actors {
    print(i)
}
